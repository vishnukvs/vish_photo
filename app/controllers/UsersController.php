	{
		$data = Input::all();
		$rules = array(
			'firstname'=>'required',
			'lastname'=>'required',
			'email'=>'required|email',
			'password'=>'required'
			);
		$validate = Validator::make($data,$rules);
		$user = new User;
		$user->firstname = $data['firstname'];
		$user->lastname = $data['lastname'];
		$user->email = $data['email'];
		$user->password = Hash::make($data['password']);
		$user->save();
		return Redirect::to('/home');		
	}


